// Get companies for a college
export const getCollegeCompanies = (collegeId) => {
  const college = placementDataEnhanced.find(c => c.collegeId === collegeId);
  return college ? college.companies : {};
};

// Get company statistics for a college
export const getCompanyStats = (collegeId, companyName) => {
  const college = placementDataEnhanced.find(c => c.collegeId === collegeId);
  if (college && college.companies[companyName]) {
    return college.companies[companyName];
  }
  return null;
};

// Filter colleges by type (IIT, NIT, IIIT)
export const getCollegesByType = (type) => {
  return placementDataEnhanced.filter(c => c.type === type);
};
